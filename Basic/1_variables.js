// Variables in JavaScript

// 'const' is used to declare a constant variable (its value cannot be changed)
const id = 33432425; // A unique ID that stays the same

// 'const' is also used for strings (text values)
const username = "james_bonds"; // A username that does not change

// 'let' is used to declare a variable that can be changed later
let age = 43; // Age can change over time
let city = "New York"; // The city name can also be updated

age = 50; // Here the age is updated and new age will be displayed.

// 'console.log()' is used to print values in the console
console.log([id, username, age, city]); // Logs an array of all variables

// 'console.table()' displays data in a table format for better readability
console.table([id, username, age, city]); // Displays variables in a tabular form
